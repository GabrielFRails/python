#soma
resultado = 4+4
print('resultado = %d.' %resultado)
resultado = 4 + 4.0
print('resultado = %.1f.' %resultado)

#subtração
print(5-1)

#multiplicação
resultado = 5*2
print('resultado = %.1f.' %resultado)

#divisão
resultado = 10/2
print('resultado = %.1f.' %resultado)

#resto
resultado = 10%3
print('resultado = %.1f.' %resultado)

#potência
print(pow(4,2))
print(4 ** 2)

#Conversão de tipos de dados
number = 9
float(number)
print('convertendo 9 para float = %.1f' %number)

number = 9.0
int(number)
print('convertendo 9.0 para int -> %d' %number)

#valor absoluto
print(abs(-8))

#arredondamento
print(round(3.14151922,2))